    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        String username = txtUsername.getText().trim();
        String password = String.valueOf(txtPassword.getPassword()).trim();

        Radnik radnik = new Radnik();
        radnik.setUsername(username);
        radnik.setPassword(password);

        try {
            Radnik loggedIn = UIController.getInstance().login(radnik);
            JOptionPane.showMessageDialog(this, "Dobrodo≈°li , " + loggedIn.getIme() + "!");
            this.dispose();
            new FormaMain(loggedIn).setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, ""+e.getMessage());
            System.out.println("Error during log in : " + e.getMessage());
        }
    }//GEN-LAST:event_btnLogInActionPerformed

